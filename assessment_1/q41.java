class A{
    public static void main(String args[]){

        int diameter=30;
        float radius=diameter/2f;
        int height=50;
        double pie=3.1416;

        //volume of cylinder (pie*r^2*h)
        double volume=((pie*radius*radius*height));

        System.out.println("volume of cylinder is :"+volume+"cm^3");
    }
}